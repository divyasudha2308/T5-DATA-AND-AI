def total_trip_bill(*args):
    total = 0
    for amount in args:
        total += amount
    return total


def show_trip_summary(**kwargs):
    phone = kwargs["phone"]
    masked_phone = phone[:2] + "******" + phone[-2:]

    print("\nTRIP SUMMARY")
    print("Name:", kwargs["name"].title())
    print("Age:", kwargs["age"])
    print("Phone:", masked_phone)

    print("\nPlaces Visited:")
    for place in kwargs["places"]:
        print("-", place)

    print("\nRestaurants:")
    for restaurant in kwargs["restaurants"]:
        print("-", restaurant)

    print("\nBills:")
    count = 1
    for amount in kwargs["bills"]:
        print("Bill", count, ": Rs.", amount)
        count += 1

    print("\nTotal Trip Bill: Rs.", kwargs["total"])


